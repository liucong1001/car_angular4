/**
 * 代码项模型
 */
export class Codeitem {
  /**
   * 代码项所属代码集
   */
  public codemap: string;
  /**
   * 代码项代码
   */
  public code: string;
  /**
   * 代码项描述
   */
  public name: string;
  /**
   * 代码项排序
   */
  public sort: number;
  /**
   * 代码项的值
   */
  public value: string;
}
